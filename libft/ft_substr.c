#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;
	size_t	slen;

	i = 0;
	slen = 0;
	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (slen < (size_t)start)
		len = 0;
	if (!(str = (char*)malloc((len + 1) * sizeof(char))))
		return (NULL);
	while (i < len && s[start])
	{
		str[i] = s[start];
		i++;
		start++;
	}
	str[i] = 0;
	return (str);
}
